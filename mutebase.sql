DROP TABLE IF EXISTS abilities CASCADE;
CREATE TABLE abilities(
  id serial primary key,
  ability varchar,
  abilityi varchar,
);

DROP TABLE IF EXISTS brands CASCADE;
CREATE TABLE brands(
  id serial primary key,
  brand varchar,
  brandi varchar,
  pref varchar,
  unpref varchar,
);

DROP TABLE IF EXISTS gear CASCADE;
CREATE TABLE gear(
  id serial primary key,
--name
  gearname varchar,
--image links
  geari varchar,
--ability data
  brand varchar,
  main varchar,
  
--color
  color1 varchar,
  color2 varchar,
--owner
  annieshop boolean
--type
  catagory varchar,
  winter boolean,
  spring boolean,
  summer boolean,
  fall boolean,
--misc
  costig integer,
  rarity integer,
);

